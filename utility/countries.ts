Object.keys(countries).forEach((code) => {
    // We do this just so we don't have to hardcode every code *and* every
    // emoji, because the emojis are just the codes except that they use a
    // special unicode char.
    const codeSplit = code.split("");
    let emoji: number[] = [];
    codeSplit.forEach((part) => {
        emoji.push(codePoints[part]);
    });
    countries[code].emoji = String.fromCodePoint(...emoji);
});

